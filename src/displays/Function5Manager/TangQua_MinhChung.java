    private void btnTaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaiActionPerformed
        if (check) {
            if (JOptionPane.showConfirmDialog(null, "Tải minh chứng mới?", "Warning!", JOptionPane.YES_NO_OPTION) == 0) {
                JFileChooser file = new JFileChooser();
                file.setCurrentDirectory(new File(System.getProperty("user.home")));
                int result = file.showSaveDialog(null);
                if (result == JFileChooser.APPROVE_OPTION) {
                    File selectedFile = file.getSelectedFile();
                    path = selectedFile.getAbsolutePath();
                    txtImage.setIcon(ResizeImage(path));
                }
            }

        } else {
            JFileChooser file = new JFileChooser();
            file.setCurrentDirectory(new File(System.getProperty("user.home")));
            int result = file.showSaveDialog(null);
            if (result == JFileChooser.APPROVE_OPTION) {
                File selectedFile = file.getSelectedFile();
                path = selectedFile.getAbsolutePath();
                txtImage.setIcon(ResizeImage(path));
            }
        }
    }//GEN-LAST:event_btnTaiActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        try {
            tq.setMinhChung(new FileInputStream(new File(path)));
            JOptionPane.showMessageDialog(null, "Lưu thành công!");
            tqController.capNhatMinhChungTQ(tq, tq.getIdPhanQua(), tq.getMaHoKhau(), tq.getHoTen());
            MainFrame.it.setEnabled(true);
            dispose();
        } catch (NullPointerException | FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Lưu không thành công", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnLuuActionPerformed

