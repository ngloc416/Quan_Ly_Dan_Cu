    public TangQua_MinhChung(TangQuaModel tq) {
        initComponents();
        this.tq = tq;
        if (tq.getMinhChung() != null) {
            check = true;
            String filePath = "D:/minhchung.jpg";
            OutputStream outputStream = null;
            try {
                outputStream = new FileOutputStream(filePath);
            } catch (FileNotFoundException ex) {
            }
            int bytesRead;
            byte[] buffer = new byte[4096];
            try {
                while ((bytesRead = tq.getMinhChung().read(buffer)) != -1) {
                    try {
                        outputStream.write(buffer, 0, bytesRead);
                    } catch (IOException ex) {
                    }
                }
            } catch (IOException ex) {
            }
            txtImage.setIcon(ResizeImage(filePath));
        }

        setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        this.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                MainFrame.it.setEnabled(true);
                dispose();
            }
        });
    }

    public ImageIcon ResizeImage(String imgPath) {
        ImageIcon icon = new ImageIcon(imgPath);
        Image img = icon.getImage();
        Image newImage = img.getScaledInstance(txtImage.getWidth(), txtImage.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImage);
        return image;
    }

