    private void btnKiemTraNgKhaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKiemTraNgKhaiActionPerformed
        if (txtCmndNgKhai.getText().trim().length() != 8
                && !(txtCmndNgKhai.getText().trim().length() == 5 && txtCmndNgKhai.getText().trim().equals("admin"))
                && txtCmndNgKhai.getText().trim().length() != 9
                && txtCmndNgKhai.getText().trim().length() != 12) {
            check1 = false;
            JOptionPane.showMessageDialog(rootPane, "Định dạng số CMT/CCCD không hợp lệ!", "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (txtCmndNgMat.getText().trim().equals(txtCmndNgKhai.getText().trim())) {
            JOptionPane.showMessageDialog(rootPane, "Số CMT/CCCD của người khai và người mất phải khác nhau!", "Warning!", JOptionPane.WARNING_MESSAGE);
        } else {
            if (ktController.checkCmndSs_Cn(txtCmndNgKhai.getText().trim())) {
                if ("admin".equals(txtCmndNgKhai.getText().trim())) {
                    txtHoTenNgKhai.setText("admin");
                    txtSđt.setText("");
                    JOptionPane.showMessageDialog(rootPane, "Kiểm tra thành công!");
                    check1 = true;
                } else {
                    if (!txtHoTenNgKhai.getText().trim().isEmpty() && !txtSđt.getText().trim().isEmpty()) {
                        check1 = true;
                        if (txtSđt.getText().trim().length() != 10) {
                            check1 = false;
                            JOptionPane.showMessageDialog(rootPane, "Định dạng số điện thoại không hợp lệ!", "Warning!", JOptionPane.WARNING_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Số CMT/CCCD chưa có trong hệ thống! Vui lòng nhập họ tên "
                                + "và số điện thoại liên lạc!", "Warning!", JOptionPane.WARNING_MESSAGE);
                    }
                    if (check1) {
                        JOptionPane.showMessageDialog(rootPane, "Kiểm tra thành công!");
                    }
                }
            } else {
                check1 = true;
                JOptionPane.showMessageDialog(rootPane, "Kiểm tra thành công!");

                ngKhai = ktController.findByCondition(txtCmndNgKhai.getText().trim()).get(0);
                txtHoTenNgKhai.setText(ngKhai.getHoTen());
                txtSđt.setText("");
            }
        }
    }//GEN-LAST:event_btnKiemTraNgKhaiActionPerformed

